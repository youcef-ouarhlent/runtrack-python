def différents_langage(langage):
    if langage == "JavaScript" :
        print("tu es développeur web")
    elif langage == "python" :
        print("tu es développeur IA")
    elif langage == "java" :
        print("tu es développeur logiciel")
    elif langage == "reactjs" :
        print("tu es développeur mobile")
    else :
        print("un jour, je serai le meilleur développeur...")      
différents_langage("JavaScript")   
différents_langage("python") 
différents_langage("java") 
différents_langage("reactjs") 
différents_langage("html")      
